/*   ::::    some terms to know

01:  declare a variable
02:  Assign a value 
03:  Redeclare a variable
04:  ReAssign a value

*/








// :::   01: var :::

var name = 'Abid'; //declaring and Assigning value
console.log(name) //  will print Abid :

var name = 'Ali';  // redeclare and reAssigning value
console.log(name) // will print Ali

name = 'khokhar'; // reAssigning value
console.log(name) // will print khokhar

// ----------------------------------------------------------------------------------------------------------------//

// :::   02: let :::

let city =  "Karachi" //declaring and Assigning value
console.log(city) //  will print Abid :

// let city = 'Lahore';  // redeclare and reAssigning value     // xx we can not redeclare it  ///xx
console.log(city) // will print Ali

city = "Islamabad" // reAssigning value
console.log(city) // will print khokhar



// ----------------------------------------------------------------------------------------------------------------//

// :::   03: const :::

const country = 'Pakistan' //declaring and Assigning value
console.log(country) //  will print Abid :

 // const country = 'USA' // redeclare and reAssigning value     // xx we can not redeclare it  ///xx
console.log(country) // will print Ali

// country = 'China' // reAssigning value                      // xx  also we can not reAssign it // xx
console.log(country) // will print khokhar



